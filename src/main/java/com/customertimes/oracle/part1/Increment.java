package com.customertimes.oracle.part1;

public class Increment {
    public static void main(String[] args) {

        int i = 3;

        i++;

        System.out.println("i=4; i = " + i);

        ++i;

        System.out.println("i=5; i = " + i);

        System.out.println("i=6; i = " + ++i);

        System.out.println("i=6; i = " + i++);

        System.out.println("i=7; i = " + i);
    }
}
